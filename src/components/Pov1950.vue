            ### 1953년 7월: **휴전협정 직전까지 이어진 전투들**

            1953년, 전쟁은 마침내 종결을 향해 나아가고 있었지만, 여전히 고지 전투는 계속되었다. 휴전협정이 논의되고 있었음에도 불구하고, 고지를 차지하기 위한 상징적인 전투가 남과 북 양측에서
            치열하게 벌어졌다. 이 시기에는 금성전투와 같은 대규모 전투도 있었다. 고지를 빼앗기지 않으려는 양측의 치열한 공방전은 실제 전쟁의 결과와는 별개로 상징성을 지니고 있었다. 이로 인해 병사들은
            끝없는 전투에 내몰리며 심리적 고통을 겪었다.

