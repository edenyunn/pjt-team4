            위험한 상황에서도 작전을 성공시키기 위해 목숨을 걸고 싸운 사람들의 이야기를 담고 있다. 첩보원들이 수행한 비밀 작전과 상륙작전의 긴박한 전개를 통해 전쟁의 전략적 측면을 부각시킨다. 이 영화는
