            ## 6. 현대적 함의: 대한민국 정치사에서 12.12의 중요성

            12.12 사태는 대한민국 군부의 정치 개입과 정권 장악을 상징하는 중대한 사건으로, 현대 정치사에서 군부와 정치의 관계를 재조명하게 한 계기다. 이 사건을 통해 군부는 정치권에서 결정적인
            영향력을 행사하며 권력을 쥐었고, 이는 이후 1980년대 군사 정권으로 이어졌다. 12.12 사태는 민주주의 후퇴와 권위주의 정권의 부상을 알린 사건으로, 군의 정치적 중립성에 대한 논의와
            제도적 변화를 촉발시켰다.

            오늘날 12.12 사태는 정치적 군사 행위의 위험성과 함께 민주주의와 권위주의 사이에서 권력의 균형을 어떻게 유지해야 하는지에 대한 중요한 교훈을 제공한다. 이 사건은 군부의 권력 남용을
            경계하게 하고, 이후 군의 정치적 중립성을 확립하는 데 중요한 영향을 미쳤다. 12.12는 대한민국이 민주적 체제로 가는 과정에서 피할 수 없었던 군사적 개입의 역사를 이해하는 데 필수적인
            사건이다.

            ## 7. **사건 이후의 정치적 변화**: 군부 독재로의 전환

            전두환과 신군부는 12.12 사태를 통해 군사력을 동원하여 권력을 장악한 후, 군사 정권을 수립하며 대한민국의 정치 체제를 강하게 흔들었다. 이 과정에서 민주적 절차는 무시되었고, 권위주의적인
            정치 체제가 1980년대에 들어서면서 강화되었다. 5.18 광주 민주화 운동은 이 권위주의에 맞선 대표적인 시민 저항이었다. 광주에서 시민들은 민주주의를 요구했으나, 군부는 강경하게 이를
            진압하며 국가의 권력을 더욱 공고히 했다.

            군부는 광주 민주화 운동을 무력으로 진압함으로써 정권에 대한 도전을 억압했고, 이는 이후 전두환이 이끄는 권위주의 체제를 공고히 하는 중요한 계기가 되었다. 이 시기에는 언론과 정치 활동이
            통제되었으며, 군부는 국가보위비상대책위원회 등 강압적인 통치 기관을 통해 정권을 유지했다.

            그러나 이러한 군사 정권의 억압은 시민 사회의 저항을 억누르기만 한 것이 아니었다. 오히려 민주주의를 향한 국민들의 열망이 점점 더 강해졌고, 광주 민주화 운동의 희생과 그 후의 탄압은
            1980년대 후반의 민주화 운동으로 이어졌다. 특히 1987년 6월 항쟁은 군사 정권의 몰락을 이끌어 내고, 대한민국이 민주주의 체제로 전환되는 결정적인 계기가 되었다.

            전두환의 군사 정권은 10.26 사건과 12.12 사태 이후 발생한 정치적 혼란 속에서 등장했으나, 그 결과는 오히려 대한민국 민주화 운동의 불씨가 되어 군부 독재의 종말을 앞당기는 중요한
            역사적 변화를 일으켰다.

