<script>
export default {
  name: "MovieModal",
  props: {
    isOpen: {
      type: Boolean,
      required: true,
    },
    movie: {
      type: Object,
      default: null,
    },
  },
  data() {
    return {
      isPlaying: false,
      player: null,
    };
  },
  computed: {
    movieBgmId() {
      this.$nextTick(() => {
        if (window.YT && window.YT.Player) {
          this.initializePlayer();
        } else {
          // YouTube API가 아직 로드되지 않았다면 기다림
          window.onYouTubeIframeAPIReady = this.initializePlayer;
        }
      });
      return this.movie.details.backgroundMusic.youtubeId;
    },
    // isMusicReady() {
    //   return !!this.player.isMusicReady()
    // }
  },
  methods: {
    closeModal() {
      if (this.player) {
        this.player.stopVideo();
      }
      this.isPlaying = false;
      this.$emit("close");
    },
    toggleMusic() {
      if (this.player) {
        if (this.isPlaying) {
          this.player.pauseVideo();
        } else {
          this.player.playVideo();
        }
        this.isPlaying = !this.isPlaying;
      }
    },
    initializePlayer() {
      if (
        window.YT &&
        window.YT.Player &&
        this.movie?.details?.backgroundMusic
      ) {
        const iframe = document.getElementById("bgMusic");
        if (iframe) {
          this.player = new window.YT.Player("bgMusic", {
            events: {
              onStateChange: (event) => {
                // 재생 상태 변경시 isPlaying 업데이트
                this.isPlaying = event.data === window.YT.PlayerState.PLAYING;
              },
            },
          });
        }
      }
    },
  },

  beforeUnmount() {
    if (this.player) {
      this.player.stopVideo();
      this.player.destroy();
    }
  },
};
</script>

